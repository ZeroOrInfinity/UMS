package top.dcenter.ums.security.core.mdc.error;

import org.springframework.boot.autoconfigure.web.ErrorProperties;
import org.springframework.boot.autoconfigure.web.servlet.error.BasicErrorController;
import org.springframework.boot.autoconfigure.web.servlet.error.ErrorViewResolver;
import org.springframework.boot.web.servlet.error.ErrorAttributes;
import org.springframework.http.HttpStatus;
import org.springframework.http.MediaType;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import javax.servlet.http.HttpServletRequest;
import java.util.List;
import java.util.Map;

import static java.util.Objects.nonNull;
import static top.dcenter.ums.security.core.mdc.utils.MdcUtil.MDC_KEY;

/**
 * Ums 全局异常处理, 添加 MDC 链路追踪 ID
 * @author YongWu zheng
 * @version V2.0  Created by 2020.12.20 17:25
 */
@RestController
public class UmsErrorController extends BasicErrorController {

    public UmsErrorController(ErrorAttributes errorAttributes, ErrorProperties errorProperties, List<ErrorViewResolver> errorViewResolvers) {
        super(errorAttributes, errorProperties, errorViewResolvers);
    }

    @Override
    @RequestMapping(value = "", produces = {MediaType.APPLICATION_JSON_VALUE})
    public ResponseEntity<Map<String, Object>> error(HttpServletRequest request) {

        HttpStatus status = getStatus(request);
        if (status == HttpStatus.NO_CONTENT) {
            return new ResponseEntity<>(status);
        }
        Map<String, Object> body = getErrorAttributes(request, getErrorAttributeOptions(request, MediaType.ALL));
        Object mdcTraceId = request.getAttribute(MDC_KEY);
        if (nonNull(mdcTraceId)) {
            body.put("mdcTraceId", mdcTraceId);
        }
        return new ResponseEntity<>(body, status);
    }
}
